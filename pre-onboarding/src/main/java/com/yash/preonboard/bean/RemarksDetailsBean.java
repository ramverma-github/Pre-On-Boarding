package com.yash.preonboard.bean;

import java.io.Serializable;
import java.util.Date;

import com.google.gson.annotations.Expose;

/**
 * @author himanshu.chaturvedi
 *
 */

public class RemarksDetailsBean implements Serializable {

	private static final long serialVersionUID = -5349566694604829379L;

	@Expose
	private Integer remarkId;

	@Expose
	private String remarks;

	@Expose
	private String createdBy;

	@Expose
	private Date createdDate;

	@Expose
	private CandidateDetailsBean candidateDetailsBean;

	
	
	public CandidateDetailsBean getCandidateDetailsBean() {
		return candidateDetailsBean;
	}

	public void setCandidateDetailsBean(CandidateDetailsBean candidateDetailsBean) {
		this.candidateDetailsBean = candidateDetailsBean;
	}

	
	public Integer getRemarkId() {
		return remarkId;
	}

	
	public void setRemarkId(Integer remarkId) {
		this.remarkId = remarkId;
	}

	public String getRemarks() {
		return remarks;
	}

	
	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}

	
	public String getCreatedBy() {
		return createdBy;
	}

	
	public void setCreatedBy(String createdBy) {
		this.createdBy = createdBy;
	}

	
	public Date getCreatedDate() {
		return createdDate;
	}

	
	public void setCreatedDate(Date createdDate) {
		this.createdDate = createdDate;
	}

}
